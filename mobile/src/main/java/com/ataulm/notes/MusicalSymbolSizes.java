package com.ataulm.notes;

class MusicalSymbolSizes {

    final Size note;
    final Size sharp;
    final Size flat;
    final Size natural;

    MusicalSymbolSizes(Size note, Size sharp, Size flat, Size natural) {
        this.note = note;
        this.sharp = sharp;
        this.flat = flat;
        this.natural = natural;
    }

}
